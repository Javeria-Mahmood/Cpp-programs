#include <iostream>
using namespace std;
// ANSI escape codes for text colors
#define RED     "\033[31m"      /* Red */
int main() 
{
	while(true)
	{
    cout<<RED<<"   ****	    ****"<<endl;
    cout<<RED<<" *******   *******"<<endl;
    cout<<RED<<"********* *********"<<endl;
    cout<<RED<<"*******************"<<endl;
    cout<<RED<<" *****************"<<endl;
    cout<<RED<<"  ***************"<<endl;
    cout<<RED<<"   *************"<<endl;
    cout<<RED<<"    ***********"<<endl;
    cout<<RED<<"     *********"<<endl;
    cout<<RED<<"       *****"<<endl;
    cout<<RED<<"        ***"<<endl;
    cout<<RED<<"         *"<<endl;
    cout<<"                     "<<endl;
    cout<<"                      "<<endl;
}
    return 0;
}
